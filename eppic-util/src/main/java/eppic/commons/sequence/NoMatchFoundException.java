package eppic.commons.sequence;

public class NoMatchFoundException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	public NoMatchFoundException() {
	}

	public NoMatchFoundException(String arg0) {
		super(arg0);
	}

	public NoMatchFoundException(Throwable arg0) {
		super(arg0);
	}

	public NoMatchFoundException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

}
